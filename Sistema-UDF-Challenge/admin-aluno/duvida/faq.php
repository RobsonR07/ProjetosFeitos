
<h1>Perguntas Frequentes - FAQ</h1>

<div class="line"><br/></div>

<b>1 - O que é o UDF Challenge?</b><br/><br/>

R:<br/> O UDF Challenge é uma aplicação web voltada para os alunos do curso de TI, que possibilita a resolução de desafios/questões propostas pelos seus respectivos professores.

<div class="line"><br/></div>

<b>2 - Quem desenvolveu o UDF Challenge?</b><br/><br/>

R:<br/> Estudantes do primeiro semestre do curso de Análise e Desenvolvimentos e Sistema na UDF.

<div class="line"><br/></div>

<b>3 - Quais as linguagens utilizadas para desenvolver o UDF Challenge?</b><br/><br/>

R:<br/> Foram utilizadas primariamente: PHP, HTML, CSS, JavaScript com o framework Bootstrap 4.

<div class="line"><br/></div>

<b>4 - Os desafios valem nota?</b><br/><br/>

R:<br/> Depende. Caso o professor que utilize o sistema estipular que os seus desafios integraram a sua avaliação (A2), então sim, os desafios valerão nota.

<div class="line"><br/></div>

